// exerciseMeta.js — определяет группы мышц и снаряд по названию упражнения.
// Эвристика по ключевым словам (русский), порядок правил важен.
// Использование:
//   import { muscleGroup, equipment } from './exerciseMeta'
//   muscleGroup('Подъём штанги на бицепс')  -> 'arms'
//   equipment('Тяга верхнего блока')         -> { key:'machine', label:'Тренажёр' }

const norm = (s) => (s || '').toLowerCase().replace(/ё/g, 'е').trim()

// ---- ГРУППЫ МЫШЦ ----
// Проверяем по порядку, первое совпадение выигрывает.
const GROUP_RULES = [
  ['cardio',    /бег|дорожк|велосипед|эллипс|скакалк|кардио|гребл|орбитрек|степпер|берпи|бурпи/],
  // пресс: подъём ног в висе тоже сюда
  ['abs',       /пресс|скручив|планк|живот|косы|вакуум|русский твист|ролик|подъ?ем ног.*вис|в висе|уголок/],
  // ноги: приседы, выпады, жим ногами, махи гирей (свинг), румынская/мёртвая тяга
  ['legs',      /присед|выпад|жим ног|разгибание ног|сгибание ног|икр|голен|ягодиц|носк|гакк|плие|зашагив|румынск|мертвая тяга|мертвую тягу|свинг|мах.*гир|гир.*мах|сумо|болгарск|ягодичн/],
  // плечи-приоритет: протяжка / тяга к подбородку = дельты, не спина
  ['shoulders', /к подбородку|подбородк|протяжк/],
  // спина: тяги, подтягивания, становая, шраги, пуловер, гиперэкстензия
  ['back',      /тяг|подтягив|широчайш|спин|становая|становую|шраг|пуловер|гиперэкстенз|т-гриф|гребн/],
  // грудь: жим лёжа, разведения лёжа, сведения, отжимания, бабочка, кроссовер
  ['chest',     /жим лежа|жим.*лежа|лежа.*жим|разведени.*лежа|сведени|бабочк|кроссовер|отжимани|пуш-ап|от груди|грудн|груди/],
  // плечи: жим стоя/сидя/над головой, армейский, махи, дельты, протяжка, тяга к подбородку
  ['shoulders', /жим стоя|жим сидя|жим.*сидя|жим.*стоя|над головой|армейск|арнольд|швунг|мах|дельт|плеч|протяжк|к подбородку|разведени.*стоя|разведени.*сторон/],
  // руки: бицепс, трицепс, сгибание/разгибание рук, молот, французский, брусья, отжимания узким
  ['arms',      /бицепс|трицепс|сгибание рук|разгибание рук|молот|французск|на бицепс|на трицепс|брусь|концентрир|скотт|разгибание на|сгибание на/],
]

export function muscleGroup(name) {
  const s = norm(name)
  for (const [group, rx] of GROUP_RULES) {
    if (rx.test(s)) return group
  }
  // запасной разбор для «жим …» без явной позиции — считаем грудью (базовый жим)
  if (/жим/.test(s)) return 'chest'
  return null // неизвестно — иконка покажет нейтральный силуэт
}

// ---- СНАРЯД ----
const EQUIP_RULES = [
  ['barbell',    'Штанга',    /штанг|гриф|ez-гриф|w-гриф/],
  ['dumbbell',   'Гантели',   /гантел|молот|арнольд/],
  ['kettlebell', 'Гиря',      /гир[еиюя]|гирей/],
  ['machine',    'Тренажёр',  /блок|тренаж[её]?р|хаммер|машин|кроссовер|гакк|смит|рычаж|в тренаж|в раме|на тренаж|жим ног|сгибание ног|разгибание ног|в хаммере/],
  ['bodyweight', 'Своё тело', /подтягив|отжимани|брусь|планк|скручив|пресс|вис|турник|собственн|без отягощ|берпи|бурпи|уголок|пуш-ап|гиперэкстенз/],
  ['cardio',     'Кардио',    /бег|дорожк|велосипед|эллипс|скакалк|кардио|гребл|орбитрек|степпер/],
]

export function equipment(name) {
  const s = norm(name)
  for (const [key, label, rx] of EQUIP_RULES) {
    if (rx.test(s)) return { key, label }
  }
  return null // неизвестно — подпись снаряда не показываем
}

// Удобный комбо-геттер
export function exerciseMeta(name) {
  return { group: muscleGroup(name), equipment: equipment(name) }
}
