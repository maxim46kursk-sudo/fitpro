// Логика построения системного промпта AI-ассистента по питанию.
// Вынесена из AIAssistant.jsx в отдельный не-JSX модуль, чтобы её же
// можно было использовать из test-ai.js без сборки React-компонента.

export const ACTIVITY_LABELS = { sedentary: 'малоподвижный', moderate: 'умеренный', high: 'высокий' }

// Коэффициент активности применяется к итоговой норме калорий (не к граммам
// белка/жира — они рассчитаны от сухой массы и не зависят от активности).
// Если уровень не указан — считаем малоподвижным (самое консервативное допущение).
export const ACTIVITY_MULTIPLIERS = { sedentary: 1.2, moderate: 1.375, high: 1.55 }

// Возраст на сегодняшнюю дату из birthdate (YYYY-MM-DD)
export const calcAge = (birthdate) => {
  if (!birthdate) return null
  const b = new Date(birthdate)
  if (isNaN(b)) return null
  const now = new Date()
  let age = now.getFullYear() - b.getFullYear()
  const beforeBirthday = now.getMonth() < b.getMonth() || (now.getMonth() === b.getMonth() && now.getDate() < b.getDate())
  if (beforeBirthday) age--
  return age
}

export const PER_KG_BY_GOAL = {
  'Похудение':   { p: 2, c: 3, f: 1 },
  'Рельеф':      { p: 2, c: 2, f: 1 },
  'Набор массы': { p: 2, c: 5, f: 1 },
  'Поддержание': { p: 2, c: 3, f: 1 },
}

// Методика тренера: расчёт нормы КБЖУ от роста/веса/пола/цели/активности
// (см. буквальные шаги в системном промпте). Коэффициент активности
// масштабирует все три макроса ПРОПОРЦИОНАЛЬНО (не только углеводы) — первая
// версия добавляла всю разницу калорий одними углеводами, и при "Набор массы"
// (уже повышенный г/кг по цели) + высокая активность это давало нереалистичные
// 800г углеводов в день, что живые тест-персоны сразу считали бредом расчёта.
// Пропорциональное масштабирование даёт ту же итоговую калорийность
// (Б×4+У×4+Ж×9 всегда точно равно kcal), но без перекоса в один нутриент.
export const calcMacroGoals = ({ height, weight, gender, goal, activity_level }) => {
  const h = Number(height), w = Number(weight)
  if (!h || !w) return null
  const lastTwo = h % 100
  const leanMass = gender === 'female' ? h - 110 : h - 100
  const baseWeight = w > lastTwo ? leanMass : w
  const perKg = PER_KG_BY_GOAL[goal] || PER_KG_BY_GOAL['Поддержание']
  const activityMultiplier = ACTIVITY_MULTIPLIERS[activity_level] || ACTIVITY_MULTIPLIERS.sedentary
  const p = Math.round(baseWeight * perKg.p * activityMultiplier)
  const c = Math.round(baseWeight * perKg.c * activityMultiplier)
  const f = Math.round(baseWeight * perKg.f * activityMultiplier)
  const kcal = p * 4 + c * 4 + f * 9
  return { baseWeight, p, c, f, kcal, activityMultiplier }
}

// Только для честного сравнения по запросу клиента ("а сколько по научной формуле") —
// классический Миффлин-Сан Жеор. Никогда не используется для самого расчёта нормы
// (это делает исключительно calcMacroGoals) — только чтобы показать клиенту, что
// итоговые калории близки, а методика Максима не "с потолка".
export const calcMifflin = ({ height, weight, gender, birthdate, activity_level }) => {
  const h = Number(height), w = Number(weight)
  const age = calcAge(birthdate)
  if (!h || !w || age == null) return null
  const bmr = gender === 'female' ? 10 * w + 6.25 * h - 5 * age - 161 : 10 * w + 6.25 * h - 5 * age + 5
  const activityMultiplier = ACTIVITY_MULTIPLIERS[activity_level] || ACTIVITY_MULTIPLIERS.sedentary
  const tdee = Math.round(bmr * activityMultiplier)
  return { bmr: Math.round(bmr), tdee, activityMultiplier }
}

// Модель ненадёжно вычитает/усредняет числа "в уме" (проверено тестами — стабильно путает
// остаток белка/углеводов/жиров). Поэтому суммы и средние считаются здесь, на JS-стороне,
// и кладутся в промпт уже готовыми — AI должен только называть эти числа, не вычислять их сам.
const FORMAT_RULE = 'ФОРМАТ ОТВЕТА: только сплошной текст. Категорически запрещены символы в начале строк: дефис, тире, звёздочка, точка с цифрой. Любые перечисления пиши в одну строку через запятую. Правильно: Творог 150г, банан, орехи. Неправильно: перечисление с новых строк через дефисы.'

const dateLabel = (iso, today) => {
  const [y, m, d] = iso.split('-')
  return `${d}.${m}.${y}${iso === today ? ' (сегодня)' : ''}`
}

// Группирует дневник (записи за много дней) по датам, новые сверху,
// и считает итог за каждый день + отклонение от нормы — готовыми числами.
function groupDiaryByDate(diary, today, goalsKcal) {
  const byDate = {}
  for (const e of diary) {
    const d = e.date || today
    ;(byDate[d] ??= []).push(e)
  }
  const dates = Object.keys(byDate).sort((a, b) => b.localeCompare(a))
  const days = dates.map(d => {
    const entries = byDate[d]
    const kcal = entries.reduce((s, e) => s + (+e.kcal || 0), 0)
    let vsGoal = ''
    if (goalsKcal) {
      const diff = kcal - goalsKcal
      vsGoal = diff > 0 ? ` (перебор +${diff} ккал к норме ${goalsKcal})`
        : diff < 0 ? ` (недобор ${-diff} ккал к норме ${goalsKcal})`
        : ' (точно в норму)'
    }
    return { date: d, entries, kcal, vsGoal }
  })
  return days
}

function buildDiarySection(days, today) {
  if (!days.length) return 'пусто (за последние 30 дней нет ни одной записи)'
  return days.map(day => {
    const lines = day.entries.map(e => `[id:${e.id}] ${e.name} — ${e.kcal}ккал Б:${e.p}г У:${e.c}г Ж:${e.f}г`).join('\n')
    return `${dateLabel(day.date, today)}:\n${lines}\nИтого за день: ${day.kcal} ккал${day.vsGoal}`
  }).join('\n\n')
}

// Средние показатели за последние 7/30 дней — готовым числом, чтобы AI не усреднял сам.
// Если данных мало (0-1 день) — аналитика за период не имеет смысла и только
// путает нового пользователя ("откуда статистика за 30 дней, я только начал?").
function buildDiaryStatsBlock(days, today, goalsKcal) {
  if (days.length === 0) return 'СТАТИСТИКА: записей нет — пользователь только начинает вести дневник, никакой истории ещё нет.'
  if (days.length === 1) return 'СТАТИСТИКА: есть данные только за один день — истории недостаточно для аналитики за неделю/месяц. НЕ ссылайся на "статистику за 30 дней" или динамику — у пользователя её ещё физически нет, это только напугает новичка.'
  const avg = (arr) => arr.length ? Math.round(arr.reduce((s, d) => s + d.kcal, 0) / arr.length) : null
  const last7 = days.slice(0, 7)
  const over = goalsKcal ? days.filter(d => d.kcal > goalsKcal) : []
  return `СТАТИСТИКА (готовые числа — не пересчитывай сам):
Дней с записями за последние 30 дней: ${days.length}
Среднее за последние 7 дней с записями: ${avg(last7)} ккал/день
Среднее за последние 30 дней с записями: ${avg(days)} ккал/день
Дни с переборoм нормы: ${over.length ? over.map(d => `${dateLabel(d.date, today)} (+${d.kcal - goalsKcal} ккал)`).join(', ') : 'таких дней нет'}`
}

