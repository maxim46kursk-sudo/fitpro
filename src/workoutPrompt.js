// Расчётный движок прогрессии тренировок (1ПМ, формула Эпли из oneRepMax.js)
// + системный промпт AI-консультанта в режиме "Тренировки".
//
// Движок (buildExerciseAggregates/computeTargetWeight/buildAssignedSessionPlan/
// parseTemplateSets ниже) — общая математика для трёх независимых
// потребителей: WorkoutsView в App.jsx (кнопка "▶ Начать тренировку" внутри
// слота шаблонной программы — основной, боевой путь для клиентов),
// замороженного src/ConstructorView.jsx (см. docs/CONSTRUCTOR_FROZEN.md) и
// test-progression-personas.js. Единица прогрессии — одноповторный максимум
// (1ПМ), а не тоннаж: тоннаж
// несопоставим между разными повторениями (30кг×15 и вес на 6 повторений
// нельзя сравнить через суммарный вес×повторения напрямую — это даёт
// абсурдные скачки), а 1ПМ корректно учитывает нелинейность зависимости
// веса от числа повторений.
//
// Промпт (buildWorkoutSystemPrompt ниже) — режим консультанта: чат отвечает
// на вопросы по технике/методике/мифам, но НЕ считает вес, НЕ составляет
// программы и НЕ трогает дневник — весь расчёт веса теперь делает движок
// выше напрямую внутри WorkoutsView, без единого слова диалога с AI. Раньше
// движок использовался и здесь (модель получала уже посчитанный вес и сама
// писала его в дневник маркерами ADD_SET/SET_PROGRAM и т.п.) — этот путь
// закрыт полностью, см. историю правок.
import { EXERCISE_TYPE, isOneSidedExercise } from './programs.js'
import { oneRepMax, weightForReps, roundToPlate, plateStep } from './oneRepMax.js'

const FORMAT_RULE = 'ФОРМАТ ОТВЕТА: только сплошной текст. Категорически запрещены символы в начале строк: дефис, тире, звёздочка, точка с цифрой. Любые перечисления пиши в одну строку через запятую.'

// Сколько сессий подряд без реальной оценки нагрузки допустимо, прежде чем
// движок останавливает рост (кг-ось: computeTemplateScale forceHold; резинки:
// heldSteps в App.jsx runStartSlotWorkout и здесь в buildAssignedSessionPlan)
// — дальше расти "вслепую" на угаданной оценке 3 небезопасно. Общая константа
// для обоих мест, чтобы порог не мог разойтись.
export const UNRATED_STOP_AFTER = 2

// ─────────────────────────────────────────────────────────────────────────
// Шаблон сессии программы хранит подходы человекочитаемой строкой ("20 кг ×
// 15, 25 кг × 12, 25 кг × 12, 25 кг × 12"), но не всегда с весом в кг —
// вес тела ("б/в × 20"), уровень резины ("1 рез. × 15" — НАГРУЗКА, аналог
// килограммов для домашней программы, а не украшение названия, см.
// programs.js), отрицательный вес компенсации гравитрона ("-39 кг × 12")
// или просто список повторений без снаряда ("30, 30, 30, 30"). Разбираем на
// подходы {reps, templateKg, bandLevel}: reps есть всегда (повторения
// ВСЕГДА берутся из шаблона, см. PROGRESSION_RULE), templateKg — только
// когда в строке реально указан вес в кг (иначе null, для таких подходов
// 1ПМ-движок вес не считает — это не кг, считать по формуле Эпли нечего),
// bandLevel — только когда указан уровень резины (иначе null, для таких
// подходов вместо 1ПМ считает отдельная ось нагрузки — computeProgressSteps/
// computeBandTarget ниже).
//
// Экспортирована отдельно (не только для buildAssignedSessionPlan ниже) —
// App.jsx переиспользует её напрямую при запуске тренировки из слота
// программы (WorkoutsView, кнопка "▶ Начать тренировку"), чтобы не
// дублировать регулярку разбора шаблона.
// ─────────────────────────────────────────────────────────────────────────
export function parseTemplateSets(str) {
  if (!str) return []
  // Десятичная запятая ("2,5 кг × 12") нормализуется в точку ДО split(',') —
  // иначе split рвёт дробный вес на два мусорных подхода ("2" и "5 кг × 12").
  // Затрагивает только запятые МЕЖДУ ЦИФРАМИ — разделители подходов (после
  // которых пробел, не цифра) не трогает.
  return str.replace(/(\d),(\d)/g, '$1.$2').split(',').map(part => {
    const raw = part.trim()
    const kgMatch = raw.match(/(-?\d+(?:[.,]\d+)?)\s*кг\s*[×x]\s*(\d+)/)
    if (kgMatch) return { templateKg: Number(kgMatch[1].replace(',', '.')), bandLevel: null, reps: Number(kgMatch[2]) }
    const bandMatch = raw.match(/(\d+)\s*рез\.?\s*[×x]\s*(\d+)/i)
    if (bandMatch) return { templateKg: null, bandLevel: Number(bandMatch[1]), reps: Number(bandMatch[2]) }
    const repsAfterX = raw.match(/[×x]\s*(\d+)/)
    if (repsAfterX) return { templateKg: null, bandLevel: null, reps: Number(repsAfterX[1]) }
    const repsOnly = raw.match(/^(\d+)$/)
    if (repsOnly) return { templateKg: null, bandLevel: null, reps: Number(repsOnly[1]) }
    return null
  }).filter(Boolean)
}

// ─────────────────────────────────────────────────────────────────────────
// История тренировок — группировка по упражнению и сессии, с оценками
// нагрузки на рабочих подходах, последним реальным подходом (опора
// 1ПМ-движка) и сигналом разового отката.
// ─────────────────────────────────────────────────────────────────────────

// Рабочие подходы определяются СТРУКТУРНО, а не по наличию оценки: у каждого
// упражнения за сессию несколько подходов, первые — разминочные, последние
// 2 (или меньше, если записано неполно) — рабочие. Сессия — не календарный
// день, а РЕАЛЬНАЯ тренировка: строки группируются по workout_id (одна
// запись в "Мои тренировки" = одна сессия, сколько бы дней между ними ни
// прошло, и сколько бы тренировок одного упражнения ни было в один день).
// Старые строки без workout_id (до перехода на таблицу workouts, а также
// синтетические источники вроде constructor_sets, где этого столбца в
// принципе нет) группируются по date — предыдущее поведение, обратная
// совместимость. Сортировка внутри сессии — по id (порядок логирования).
// Порядок самих сессий — по МИНИМАЛЬНОМУ id строки в сессии (устойчиво к
// тому, что дата тренировки может быть проставлена задним числом, а id
// всегда отражает реальный порядок записи).
export function buildExerciseAggregates(sets) {
  const byExercise = {}
  for (const s of sets) (byExercise[s.exercise] ??= []).push(s)

  const result = {}
  for (const [name, list] of Object.entries(byExercise)) {
    const type = EXERCISE_TYPE[name] || 'compound'
    const bySession = {}
    for (const s of list) {
      const key = s.workout_id != null ? `w${s.workout_id}` : `d${s.date}`
      ;(bySession[key] ??= []).push(s)
    }
    const sessions = Object.values(bySession)
      .map(rawSets => {
        // bandLevel — нормализация из snake_case столбца БД (band_level) в
        // camelCase, как и остальные поля прогрессии (templateKg и т.п.).
        const daySets = rawSets.slice().sort((a, b) => a.id - b.id)
          .map(s => ({ ...s, bandLevel: s.band_level ?? null }))
        const workingCount = Math.min(2, daySets.length)
        const workingSets = daySets.slice(daySets.length - workingCount)
        // Оценка рабочего подхода: если клиент её не поставил, считаем 3
        // (комфортно) — скрытое допущение только для расчёта, клиенту как
        // его выбор не показываем.
        const effRatings = workingSets.map(s => s.rating ?? 3)
        // rated — была ли у сессии хотя бы одна РЕАЛЬНАЯ оценка (не
        // подмена ?? 3 выше) — основа для unratedStreak ниже, не путать с
        // effRatings (та всегда содержит числа, в том числе угаданные).
        const rated = workingSets.some(s => s.rating != null)
        return { date: daySets[0].date, sets: daySets, workingSets, effRatings, rated, minId: daySets[0].id }
      })
      .sort((a, b) => a.minId - b.minId)
    const lastSession = sessions.length ? sessions[sessions.length - 1] : null
    // Последний реальный рабочий подход упражнения — единственная опора для
    // расчёта следующего веса (см. PROGRESSION_RULE).
    const anchorSet = lastSession && lastSession.workingSets.length
      ? lastSession.workingSets[lastSession.workingSets.length - 1]
      : null
    const hardStreak = computeHardStreak(sessions)
    // Вторая, независимая ось прогрессии — уровень резины/повторения для
    // домашней программы (computeProgressSteps/computeBandTarget ниже).
    // Считается всегда (дёшево), используется только там, где у подходов
    // нет веса в кг.
    const progressSteps = computeProgressSteps(sessions)
    // Сколько последних сессий ПОДРЯД (считая с конца истории) прошли без
    // реальной оценки — если клиент долго не оценивает, движок не должен
    // молча растить вес до бесконечности на угаданной "3" (см. forceHold в
    // computeTemplateScale). Оценка в любой сессии обнуляет счётчик.
    let unratedStreak = 0
    for (let i = sessions.length - 1; i >= 0; i--) {
      if (sessions[i].rated) break
      unratedStreak++
    }
    result[name] = { type, sessions, lastSession, anchorSet, hardStreak, progressSteps, unratedStreak }
  }
  return result
}

// Слой 1 методики: процент роста 1ПМ к следующему разу зависит от того, как
// дались два последних рабочих подхода — чем легче, тем больше прибавка.
// Нет оценки → считаем 3 (+5%).
export const RATING_GROWTH_PCT = { 1: 10, 2: 7, 3: 5, 4: 3, 5: 2 }

// Слой 2 методики (откат) — портировано 1:1 из hasHardStreak в
// src/constructorPhases.js, тот же алгоритм без изменений — единственное
// отличие: здесь без пропуска baseline-сессии (там первая сессия —
// стартовый замер, тут такого понятия нет, первая тренировка идёт по
// шаблону тренера и участвует в подсчёте наравне со всеми остальными).
//
// Если по упражнению два раза подряд последний рабочий подход тяжёлый
// (оценка 4-5) — следующая тренировка получает разовый откат: 1ПМ текущего
// анкера ×0.85 (×1.15 для ассист-тренажёров, см. computeTargetWeight)
// вместо обычного роста по таблице оценок. Разовый: как только откат
// применился, счётчик подряд тяжёлых обнуляется — следующий откат возможен
// только после ДВУХ НОВЫХ тяжёлых тренировок, случившихся уже после этого
// отката. Без сброса пара, которая уже вызвала откат, при сдвиге окна на
// одну сессию вперёд повторно засчиталась бы во вторую тяжёлую пару подряд.
//
// Пересчитывается заново из истории при каждом обращении, никакого
// отдельного состояния/флага в БД не хранится.
export function computeHardStreak(sessions) {
  if (!sessions || sessions.length < 2) return false
  let streak = 0
  for (const s of sessions) {
    if (streak >= 2) streak = 0 // эта тренировка уже получила откат — счётчик с неё стартует заново
    const rating = s.effRatings.length ? s.effRatings[s.effRatings.length - 1] : 3
    streak = rating >= 4 ? streak + 1 : 0
  }
  return streak >= 2
}

// ─────────────────────────────────────────────────────────────────────────
// Вторая ось прогрессии — уровень резины (1-5) + повторения, для домашней
// программы (там, где считать 1ПМ нечего — резина или вес тела). Полностью
// независима от кг/1ПМ-оси выше: своя история, свой счётчик, своя логика
// отката. Единица здесь — не проценты от 1ПМ, а целочисленные "шаги":
// каждый шаг — это либо +2 повторения (для подходов без резины), либо (для
// подходов с резиной) 5 шагов = подъём на 1 уровень резины, остаток шагов —
// те же +2 повторения (см. computeBandTarget).
// ─────────────────────────────────────────────────────────────────────────

// Дельта шагов по оценке последнего рабочего подхода сессии (нет оценки —
// считаем 3, как и на кг-оси).
const BAND_STEP_DELTA_BY_RATING = { 1: 2, 2: 1, 3: 1, 4: 0, 5: 0 }

// Откат — тот же one-shot принцип, что и в hardStreak (см. выше): если
// текущая и предыдущая сессия ОБЕ тяжёлые (оценка >= 4), вместо обычной
// дельты применяется -2 к счётчику, и признак "предыдущая тяжёлая"
// сбрасывается — повторный откат возможен только после ДВУХ НОВЫХ тяжёлых
// сессий, случившихся уже после этого отката (третья тяжёлая подряд откат
// не повторяет). Счётчик никогда не опускается ниже 0.
export function computeProgressSteps(sessions) {
  if (!sessions || !sessions.length) return 0
  let steps = 0
  let prevHard = false
  for (const s of sessions) {
    const rating = s.effRatings.length ? s.effRatings[s.effRatings.length - 1] : 3
    const isHard = rating >= 4
    if (prevHard && isHard) {
      steps = Math.max(0, steps - 2)
      prevHard = false
    } else {
      steps = Math.max(0, steps + BAND_STEP_DELTA_BY_RATING[rating])
      prevHard = isHard
    }
  }
  return steps
}

// templateSet — { bandLevel, reps } из шаблона СЕГОДНЯШНЕЙ сессии (правило
// "повторения всегда из шаблона" действует и здесь: reps в результате —
// всегда templateSet.reps + добавка, никогда из истории).
//
// Без резины (вес тела) — растут только повторения, +2 за шаг.
// С резиной — каждые 5 шагов поднимают уровень на 1 (остаток шагов, 0-4,
// уходит в +2 повторения за штуку); уровень не может подняться выше 5 —
// то, что "не поместилось" в подъём уровня из-за потолка, тоже уходит в
// повторения (overflow), чтобы прогрессия не останавливалась на потолке
// резины.
// Повторения тоже ограничены сверху (BAND_REPS_CAP) — без этого overflow
// на упражнениях, годами застрявших на 5 уровне резины, уводил бы счётчик
// повторений в нереалистичные десятки/сотни. Уровень резины при этом растёт
// как и раньше — ограничивается только итоговое число повторений.
export const BAND_REPS_CAP = 30

export function computeBandTarget(templateSet, steps) {
  if (templateSet.bandLevel == null) {
    return { bandLevel: null, reps: Math.min(BAND_REPS_CAP, templateSet.reps + steps * 2) }
  }
  const levelUp = Math.floor(steps / 5)
  const rest = steps % 5
  const newLevel = Math.min(5, templateSet.bandLevel + levelUp)
  const overflow = (templateSet.bandLevel + levelUp) - newLevel
  return {
    bandLevel: newLevel,
    reps: Math.min(BAND_REPS_CAP, templateSet.reps + rest * 2 + overflow * 5 * 2),
  }
}

// Единица прогрессии — 1ПМ, не тоннаж (см. заголовок файла). anchorSet —
// последний реальный рабочий подход (buildExerciseAggregates), ratings —
// оценки двух последних рабочих подходов той же сессии, targetReps —
// сколько повторений требует шаблон СЕГОДНЯШНЕЙ сессии для этого подхода
// (правило "повторения всегда из шаблона"). hardStreak — булев сигнал
// разового отката (computeHardStreak); если true, вместо роста 1ПМ по
// таблице оценок применяется фиксированный откат −15% (+15% для ассист-
// тренажёров, см. ниже) от текущего анкера.
//
// Ассистирующие тренажёры (гравитрон и т.п.) хранят вес ОТРИЦАТЕЛЬНЫМ — это
// кг компенсации, а не поднятый вес: "-39кг" означает, что тренажёр помогает
// на 39кг. Признак — сам знак anchorSet.kg, отдельного флага не заводим.
// Для такого веса прогресс движется К НУЛЮ (меньше помощи = сильнее клиент),
// а не от него — поэтому направление роста инвертируется: тот же процент
// (из RATING_GROWTH_PCT или фиксированные 15% отката) применяется к МОДУЛЮ
// и УМЕНЬШАЕТ его при росте, а при откате УВЕЛИЧИВАЕТ (больше помощи).
// oneSided — упражнение, где повторения в шаблоне/истории записаны СУММОЙ
// на обе стороны (isOneSidedExercise, programs.js: выпады, болгарские,
// "одной ногой/рукой" и т.п.). Формула Эпли/Бржицки должна получать
// повторения НА СТОРОНУ, иначе завышает 1ПМ ровно вдвое от реальной
// нагрузки одной ноги/руки. Вес не трогаем — корректируются только
// повторения, идущие внутрь oneRepMax/weightForReps.
export function computeTargetWeight(anchorSet, ratings, targetReps, hardStreak, oneSided = false) {
  if (!targetReps) return null
  if (!anchorSet || !anchorSet.kg || !anchorSet.reps) return null
  const anchorKg = Number(anchorSet.kg)
  const isAssisted = anchorKg < 0
  const effReps = r => oneSided ? Math.max(1, Math.round(r / 2)) : r
  const anchorRM = oneRepMax(anchorKg, effReps(Number(anchorSet.reps)))
  if (!anchorRM) return null
  let appliedPct, factor
  if (hardStreak) {
    factor = isAssisted ? 1.15 : 0.85
    appliedPct = -15
  } else {
    const last2 = ratings.slice(-2)
    const avgRating = last2.length ? last2.reduce((a, b) => a + b, 0) / last2.length : 3
    const roundedRating = Math.min(5, Math.max(1, Math.round(avgRating)))
    appliedPct = RATING_GROWTH_PCT[roundedRating]
    factor = isAssisted ? (1 - appliedPct / 100) : (1 + appliedPct / 100)
  }
  const rawKg = weightForReps(anchorRM * factor, effReps(targetReps))
  return { kg: roundToPlate(rawKg, plateStep(rawKg)), rawKg, isDeload: !!hardStreak, appliedPct }
}

// ─────────────────────────────────────────────────────────────────────────
// computeTargetWeight выше считает вес ПОД ЗАДАННЫЕ ПОВТОРЕНИЯ — это
// корректно для одного подхода, но неверно для целого шаблона: если вызвать
// его отдельно на каждый подход шаблона (как раньше делали buildAssignedSessionPlan
// и кнопка "▶ Начать тренировку" в App.jsx), формула Эпли считает КАЖДЫЙ
// подход независимым "на отказ" на своё число повторений — а разминочный
// подход на 20 повторений это НЕ то же самое, что рабочий на отказ на 20
// повторений, это специально лёгкий вес тренера. В результате разминка
// росла в разы быстрее рабочего подхода, а подходы с одинаковыми
// повторениями (например два по 12) схлопывались в один и тот же вес,
// потому что Эпли зависит только от (вес, повторения) анкера и целевых
// повторений — форма лестницы шаблона терялась.
//
// Правильная модель: шаблон задаёт ФОРМУ нагрузки (соотношение весов между
// подходами, придуманное тренером), движок считает ОДИН коэффициент
// масштабирования на упражнение (от анкера истории к целевому 1ПМ) и
// применяет его ко ВСЕМУ шаблонному ряду весов — соотношение между
// подходами сохраняется, меняется только общий уровень нагрузки.
// ─────────────────────────────────────────────────────────────────────────

// anchorSet — последний реальный рабочий подход (buildExerciseAggregates),
// ratings — оценки двух последних рабочих подходов той же сессии,
// templateSets — распарсенный ряд подходов ТЕКУЩЕЙ сессии
// (parseTemplateSets), hardStreak — булев сигнал разового отката
// (computeHardStreak). forceHold — клиент долго (см. UNRATED_STOP_AFTER в
// App.jsx) не ставит реальную оценку: вместо роста/отката по угаданной "3"
// держим вес на уровне анкера (factorGrowth=1.0) — расти дальше вслепую
// небезопасно. Возвращает { scale, appliedPct, isDeload } или null, если
// масштабировать нечего (нет весового подхода в шаблоне, нет истории, анкер
// невалиден) — тогда вызывающий код подставляет шаблон как есть (холодный
// старт, как и раньше).
// oneSided — см. комментарий у computeTargetWeight выше: повторения
// односторонних упражнений записаны суммой на обе стороны, в 1ПМ-формулу
// подставляем половину (на сторону), вес не трогаем.
export function computeTemplateScale(anchorSet, ratings, templateSets, hardStreak, forceHold = false, oneSided = false) {
  const effReps = r => oneSided ? Math.max(1, Math.round(r / 2)) : r

  // 1. Опорный подход ШАБЛОНА — последний весовой подход в ряду (обычно самый
  // тяжёлый рабочий подход тренера), от него меряем форму лестницы.
  let templateAnchor = null
  for (let i = templateSets.length - 1; i >= 0; i--) {
    if (templateSets[i].templateKg != null) { templateAnchor = templateSets[i]; break }
  }
  if (!templateAnchor) return null
  const templateRM = oneRepMax(templateAnchor.templateKg, effReps(templateAnchor.reps))
  if (!templateRM) return null

  // 3. Опорный подход ИСТОРИИ — тот же anchorSet, что и в computeTargetWeight.
  if (!anchorSet || !anchorSet.kg || !anchorSet.reps) return null
  const anchorKg = Number(anchorSet.kg)
  const anchorRM = oneRepMax(anchorKg, effReps(Number(anchorSet.reps)))
  if (!anchorRM) return null

  // 4-5. Тот же рост/откат, что и в computeTargetWeight — тем же исходом на
  // тот же вход, чтобы движок и подстановка не расходились. Гравитрон
  // (отрицательный вес компенсации) — прогресс к нулю, см. комментарий
  // computeTargetWeight выше.
  const isAssisted = anchorKg < 0
  let appliedPct, factorGrowth
  if (forceHold) {
    factorGrowth = 1.0
    appliedPct = 0
  } else if (hardStreak) {
    factorGrowth = isAssisted ? 1.15 : 0.85
    appliedPct = -15
  } else {
    const last2 = ratings.slice(-2)
    const avgRating = last2.length ? last2.reduce((a, b) => a + b, 0) / last2.length : 3
    const roundedRating = Math.min(5, Math.max(1, Math.round(avgRating)))
    appliedPct = RATING_GROWTH_PCT[roundedRating]
    factorGrowth = isAssisted ? (1 - appliedPct / 100) : (1 + appliedPct / 100)
  }
  const targetRM = anchorRM * factorGrowth

  // 6. Коэффициент масштабирования всего шаблонного ряда: во сколько раз
  // целевой 1ПМ отличается от 1ПМ опорного подхода ШАБЛОНА. Для ассист-
  // упражнений оба 1ПМ отрицательны — знак сокращается сам, scale выходит
  // положительным без отдельной инверсии.
  const scale = targetRM / templateRM
  return { scale, appliedPct, isDeload: !forceHold && !!hardStreak }
}

// Какая из сессий назначенной программы — "сегодняшняя", и готовый вес под
// каждый её подход. AI сам не выбирает сессию (считаем здесь, по кругу
// 1,2,3...N,1,2..., от того, сколько тренировок по этой же программе уже
// реально записано в дневник, независимо от того, через AI это было или
// клиент вручную выбрал слот в приложении — оба пути называют тренировку
// одинаково: "{Программа} — тренировка N", это и есть ключ подсчёта, см.
// AIAssistant.jsx) и не считает вес сам (см. PROGRESSION_RULE) — состав,
// повторения и вес на сегодня всегда уже готовы здесь, единым куском.
//
// Структурная версия (не текст) — экспортирована отдельно, чтобы её мог
// напрямую переиспользовать не только этот текстовый промпт, но и внешние
// потребители реального движка прогрессии (например test-progression-personas.js),
// без копирования математики 1ПМ-движка.
export function buildAssignedSessionPlan(programTemplate, sessionsDone, aggregates) {
  if (!programTemplate || !programTemplate.length) return null
  const total = programTemplate.length
  const sessionIdx = (sessionsDone || 0) % total
  const slot = programTemplate[sessionIdx]

  const exercises = slot.map(ex => {
    const templateSets = parseTemplateSets(ex.sets)
    const agg = aggregates[ex.name]
    // Клиент долго не ставит оценку — тот же стоп, что и в runStartSlotWorkout
    // (App.jsx): дальше расти "вслепую" на угаданной оценке 3 небезопасно.
    // Резинки/повторения без веса эта функция не считает вообще (своя ось,
    // отдельная от computeTemplateScale) — стоп применяется только к кг-оси.
    const progressionStopped = !!(agg && agg.unratedStreak >= UNRATED_STOP_AFTER)
    // Один коэффициент на упражнение (computeTemplateScale) — масштабирует
    // ВЕСЬ шаблонный ряд весов целиком, сохраняя форму лестницы тренера
    // (см. заголовок computeTemplateScale выше). null — холодный старт
    // (нет истории, либо в шаблоне нет ни одного весового подхода) —
    // подставляем шаблон как есть, тем же путём, что и раньше.
    const scale = (agg && agg.anchorSet) ? computeTemplateScale(agg.anchorSet, agg.lastSession.effRatings, templateSets, agg.hardStreak, progressionStopped, isOneSidedExercise(ex.name)) : null
    const sets = templateSets.map(ts => {
      if (ts.templateKg == null) return { reps: ts.reps, kg: null, rawKg: null, hasWeight: false, coldStart: false, isDeload: false, appliedPct: null }
      if (!scale) return { reps: ts.reps, kg: ts.templateKg, rawKg: ts.templateKg, hasWeight: true, coldStart: true, isDeload: false, appliedPct: null }
      const rawKg = ts.templateKg * scale.scale
      return { reps: ts.reps, kg: roundToPlate(rawKg, plateStep(rawKg)), rawKg, hasWeight: true, coldStart: false, isDeload: scale.isDeload, appliedPct: scale.appliedPct }
    })
    return { name: ex.name, sets }
  })

  return { sessionNum: sessionIdx + 1, total, exercises }
}

